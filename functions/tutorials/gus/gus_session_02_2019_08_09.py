# Operators
print(1 + 1)
print(1 - 1)
print(1 * 1)
print(1 / 2)
print(4 / 5)
print(4 // 5)
print(3 // 5)
print(2 // 5)
print(4 % 5)
print(2 % 5)
print(2 ** 4)
print(2 == 2)
print(2 != 2)
print(2 == 3)
print(2 != 3)

# Constants
# Constants are the entities whose values do not change
Pi = 3.1415926535897932385
print(Pi)

# Variables
# Variables are entities that can take multiple values
x = 1
print(x)
x = 2



# Lists
# Lists are collections of objects

myList = ['Pen', 'Pencil', 'Eraser', 'Sharpener', 'Bag', 'Compass']
print(myList)

# For loops
# Loops can be used to perform repeated operations
for i in range(10):
    print(i)

# While loop
number = 1
while True:
    print(number)
    number += 1
    if number > 10:
        print("Sequence ended")
        break

number = 1
while number <= 10:
    print(number)
    number += 1
